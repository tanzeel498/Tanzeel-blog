# decorators
def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if current_user.get_id() != "1":
            abort(403, "You don't have the permission to access the requested resource. It is for ADMIN only.")
        return f(*args, **kwargs)

    return decorated_function


# routes
@app.route('/')
def home():
    posts = BlogPost.query.all()
    return render_template("index.html", all_posts=posts, logged_in=current_user.is_authenticated,
                           user=current_user)


@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if User.query.filter_by(email=request.form.get("email")).first():
            flash("Email already registered! Log In")
            return redirect(url_for('login'))
        else:
            hashed_password = generate_password_hash(password=request.form.get("password"), salt_length=10)
            new_user = User(email=request.form.get("email"), password=hashed_password, name=request.form.get("name"))
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for('home'))
    return render_template("register.html", form=form, logged_in=current_user.is_authenticated)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        user_from_db = User.query.filter_by(email=request.form.get("email")).first()
        if user_from_db:
            if check_password_hash(user_from_db.password, request.form.get("password")):
                login_user(user_from_db)
                return redirect(url_for('home'))
            else:
                flash("Incorrect password!")
                return redirect(url_for('login'))
        else:
            flash("Email doesn't exist!")
            return redirect(url_for('login'))
    return render_template("login.html", form=form, logged_in=current_user.is_authenticated)


@app.route('/logout')
def logout():
    logout_user()
    return redirect(url_for('home'))


@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    form = CommentForm()
    requested_post = BlogPost.query.get(post_id)
    if form.validate_on_submit():
        if current_user.is_anonymous:
            flash("You need to register or Login to comment")
            return redirect(url_for('login'))
        new_comment = Comment(text=request.form.get("comment"), comment_author=current_user, parent_post=requested_post)
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for("show_post", post_id=requested_post.id))
    return render_template("post.html", post=requested_post, logged_in=current_user.is_authenticated,
                           user_id=current_user.get_id(), form=form, comments=Comment.query.filter_by(post_id=post_id))


